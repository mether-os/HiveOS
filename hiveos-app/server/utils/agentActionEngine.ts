function makeExpiresAt(hours: number): Date {
  const d = new Date();
  d.setHours(d.getHours() + hours);
  return d;
}

function buildAuditLog(actorName: string): IAgentActionPlan["structuredAuditLogs"][0] {
  return {
    actorId: undefined,
    actorName,
    action: "submit",
    notes: "Plan auto-generated by HiveMind Action Engine.",
    timestamp: new Date(),
  };
}

