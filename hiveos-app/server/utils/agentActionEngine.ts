/**
 * Runs full diagnostic analysis and generates multi-step action plans.
 * Plans are stored as PROPOSED and never auto-executed.
 * Deduplicates by checking for existing non-rejected/non-expired plans with same title.
 */
export async function generateActionPlans(
  hiveId: string,
  options: GeneratePlansOptions = {}
): Promise<{ generated: number; skipped: number; plans: IAgentActionPlan[] }> {
  const confidenceThreshold = options.confidenceThreshold ?? 0.6;
  const maxPlansPerRun = options.maxPlansPerRun ?? 20;
  const expiryHours = options.expiryHours ?? 72;

  // 1. Pull workspace diagnostics (re-runs lightweight rule engine)
  const context = await getProjectContext(hiveId);
  const nodes = context.nodes || [];
  const edges = context.edges || [];
  const activeDocs = context.activeDocuments || [];

  const nodeLookup = new Map<string, WorkspaceNode>(nodes.map((n: WorkspaceNode) => [n.id, n]));

  // 2. Re-compute rule-based diagnostics inline (no LLM)
  const cycles = detectCycles(nodes, edges);

  const blockedNodes = nodes.filter((n: WorkspaceNode) => n.data?.status === "Blocked");

  // Gaps
  const gaps: KnowledgeGap[] = [];
  const docFeatureNodeIds = new Set((activeDocs as WorkspaceDoc[]).map((d) => d.nodeId).filter(Boolean));
  const features = (nodes as WorkspaceNode[]).filter((n) => n.category === "Feature");
  features.forEach((feat) => {
    if (!docFeatureNodeIds.has(feat.id)) {
      gaps.push({
        id: `gap-prd-${feat.id}`,
        title: `Missing Spec: ${feat.title}`,
        type: "feature_no_prd",
        description: `Feature node "${feat.title}" has no PRD.`,
        relatedEntities: [{ entityId: feat.id, entityType: "node", title: feat.title }],
      });
    }
  });

  const archNodes = (nodes as WorkspaceNode[]).filter((n) => n.category === "Architecture");
  archNodes.forEach((arch) => {
    const hasTech = (edges as WorkspaceEdge[]).some((e) => {
      const otherId =
        e.source === arch.id ? e.target : e.target === arch.id ? e.source : null;
      if (otherId) {
        const other = nodeLookup.get(otherId);
        return other?.category === "Tech Stack";
      }
      return false;
    });
    if (!hasTech) {
      gaps.push({
        id: `gap-tech-${arch.id}`,
        title: `Unsupported Architecture: ${arch.title}`,
        type: "arch_no_tech",
        description: `Architecture node "${arch.title}" has no tech stack link.`,
        relatedEntities: [{ entityId: arch.id, entityType: "node", title: arch.title }],
      });
    }
  });

  // Risks
  const risks: StructuredRisk[] = [];
  const riskNodes = nodes.filter(
    (n) => n.category === "Risk" && n.data?.status !== "Done" && n.data?.status !== "Resolved"
  );
  riskNodes.forEach((r) => {
    risks.push({
      id: `risk-node-${r.id}`,
      title: `Active Risk: ${r.title}`,
      severity: r.data?.priority === "High" ? "high" : "medium",
      confidence: 0.9,
      reason: r.description || "Unresolved risk item.",
      relatedEntities: [{ entityId: r.id, entityType: "node", title: r.title }],
      suggestedActions: ["Create mitigation plan"],
    });
  });
  cycles.forEach((cycle, idx) => {
    risks.push({
      id: `risk-cycle-${idx}`,
      title: `Circular Dependency Loop`,
      severity: "critical",
      confidence: 1.0,
      reason: `Cycle: ${cycle.map((id) => nodeLookup.get(id)?.title || id).join(" -> ")}`,
      relatedEntities: cycle.map((id) => ({
        entityId: id,
        entityType: "node",
        title: nodeLookup.get(id)?.title || id,
      })),
      suggestedActions: ["Sever cycle back-edge"],
    });
  });
  blockedNodes.forEach((bNode) => {
    risks.push({
      id: `risk-blocked-${bNode.id}`,
      title: `Work Blocked: ${bNode.title}`,
      severity: "high",
      confidence: 0.95,
      reason: `"${bNode.title}" is blocked.`,
      relatedEntities: [{ entityId: bNode.id, entityType: "node", title: bNode.title }],
      suggestedActions: ["Investigate blockers", "Assign owner"],
    });
  });

  // Active recommendations from DB
  const activeRecs = await HiveMindRecommendation.find({
    hiveId: new mongoose.Types.ObjectId(hiveId),
    status: "active",
  })
    .lean()
    .exec();

  // Unassigned nodes
  const unassignedNodes = (nodes as WorkspaceNode[]).filter(
    (n) =>
      (n.category === "Feature" || n.category === "Task") &&
      !n.data?.owner &&
      !n.data?.assignee
  );

  // 3. Build candidate plan payloads
  const candidatePlans: Partial<IAgentActionPlan>[] = [];

  // From gaps
  for (const gap of gaps.filter((g) => g.type === "feature_no_prd")) {
    candidatePlans.push(planFromGapFeatureNoPrd(gap, expiryHours));
  }
  for (const gap of gaps.filter((g) => g.type === "arch_no_tech")) {
    candidatePlans.push(planFromGapArchNoTech(gap, expiryHours));
  }

  // From risks — cycles first (highest priority)
  for (const risk of risks.filter((r) => r.id.startsWith("risk-cycle-"))) {
    candidatePlans.push(planFromRiskCycle(risk, expiryHours));
  }
  for (const risk of risks.filter((r) => r.id.startsWith("risk-blocked-"))) {
    candidatePlans.push(planFromRiskBlocked(risk, expiryHours));
  }

  // From unassigned nodes
  for (const node of unassignedNodes.slice(0, 5)) {
    candidatePlans.push(planFromUnassignedNode(node, expiryHours));
  }

  // From active recommendations
  for (const rec of activeRecs) {
    const plan = planFromRecommendation(
      rec as unknown as RecommendationLean,
      expiryHours
    );
    if (Object.keys(plan).length > 0) {
      candidatePlans.push(plan);
    }
  }

  // 4. Filter by confidence threshold
  const qualifiedPlans = candidatePlans
    .filter((p) => p.title && (p.confidence ?? 0) >= confidenceThreshold)
    .slice(0, maxPlansPerRun);

  // 5. Deduplicate — check for existing proposed/approved plans with same title
  const existingTitles = new Set<string>();
  const existing = await AgentActionPlan.find({
    hiveId: new mongoose.Types.ObjectId(hiveId),
    status: { $in: ["proposed", "approved"] },
  })
    .select("title")
    .lean()
    .exec();
  existing.forEach((p) => existingTitles.add(p.title));

  let generated = 0;
  let skipped = 0;
  const createdPlans: IAgentActionPlan[] = [];

  for (const planData of qualifiedPlans) {
    if (!planData.title) { skipped++; continue; }
    if (existingTitles.has(planData.title)) { skipped++; continue; }

    try {
      const plan = await AgentActionPlan.create({
        hiveId: new mongoose.Types.ObjectId(hiveId),
        status: "proposed",
        ...planData,
      });
      createdPlans.push(plan);
      existingTitles.add(planData.title);
      generated++;
    } catch (err) {
      console.error("[ActionEngine] Error creating plan:", err);
      skipped++;
    }
  }

  return { generated, skipped, plans: createdPlans };
}

