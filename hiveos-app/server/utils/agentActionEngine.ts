/** Minimal shape of a lean graph node returned by getProjectContext() */
interface WorkspaceNode {
  id: string;
  title: string;
  category: string;
  description?: string;
  data?: { status?: string; owner?: string; assignee?: string; priority?: string };
}

/** Minimal shape of a lean graph edge returned by getProjectContext() */
interface WorkspaceEdge {
  id: string;
  source: string;
  target: string;
}

/** Minimal shape of a lean active document returned by getProjectContext() */
interface WorkspaceDoc {
  id: string;
  nodeId?: string;
}

/** Mongoose recommendation lean object used in plan generation */
interface RecommendationLean {
  _id: mongoose.Types.ObjectId;
  title: string;
  reason: string;
  type: string;
  confidence: number;
  status: string;
  suggestedActions: string[];
  relatedEntities: Array<{ entityId: string; entityType: string; title: string }>;
}

export interface ActionSimulationResult {
  planId: string;
  // Graph deltas
  nodesAdded: number;
  nodesModified: number;
  nodesDeleted: number;
  // Diagnostic deltas
  risksResolved: number;
  risksCreated: number;
  gapsResolved: number;
  gapsCreated: number;
  ownershipChanges: number;
  dependencyChanges: number;
  // Health & momentum estimates
  estimatedHealthImpact: number;   // delta from current health
  estimatedMomentumImpact: number; // delta from current momentum
  // Affected entity summary
  affectedEntityCount: number;
  affectedEntities: Array<{ entityId: string; entityType: string; title: string; changeType: string }>;
  // Risk analysis
  reversibleSteps: number;
  irreversibleSteps: number;
  overallRiskLevel: "low" | "medium" | "high" | "critical";
}

export interface GeneratePlansOptions {
  confidenceThreshold?: number; // default 0.6
  maxPlansPerRun?: number;       // default 20
  expiryHours?: number;          // default 72
}

// ---------------------------------------------------------------------------
// Quality Score Calculator
// ---------------------------------------------------------------------------

/**
 * Computes actionQualityScore (0–100) based on:
 * - confidence (40%)
 * - reversibility of steps (30%)
 * - entity count — fewer affected = more surgical = better (15%)
 * - historical effectiveness proxied by recommendation confidence (15%)
 */
function computeActionQualityScore(
  confidence: number,
  steps: IActionStep[],
  affectedEntityCount: number
): number {
  const confidenceScore = confidence * 100 * 0.4;

  const totalSteps = steps.length;
  const reversibleSteps = steps.filter((s) => s.reversibility === "reversible").length;
  const reversibilityScore =
    totalSteps > 0 ? (reversibleSteps / totalSteps) * 100 * 0.3 : 60 * 0.3;

  // Prefer plans affecting 1–5 entities (surgical), penalize > 10
  const entityScore =
    affectedEntityCount <= 1
      ? 100
      : affectedEntityCount <= 5
      ? 85
      : affectedEntityCount <= 10
      ? 60
      : 35;
  const entityContrib = entityScore * 0.15;

  // Historical effectiveness proxy: use confidence as a stand-in
  const historicalScore = confidence * 100 * 0.15;

  const raw = confidenceScore + reversibilityScore + entityContrib + historicalScore;
  return Math.min(100, Math.max(0, Math.round(raw)));
}

// ---------------------------------------------------------------------------
// Risk Score Calculator
// ---------------------------------------------------------------------------

function computeRiskScore(riskLevel: "low" | "medium" | "high" | "critical"): number {
  const map: Record<string, number> = {
    low: 20,
    medium: 45,
    high: 70,
    critical: 95,
  };
  return map[riskLevel] ?? 50;
}

