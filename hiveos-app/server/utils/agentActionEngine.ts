/**
 * Simulates the impact of approving an action plan without executing any changes.
 * Returns a rich delta report for display in the UI's impact preview.
 */
export async function simulateActionPlan(
  hiveId: string,
  planId: string
): Promise<ActionSimulationResult | null> {
  const plan = await AgentActionPlan.findOne({
    _id: new mongoose.Types.ObjectId(planId),
    hiveId: new mongoose.Types.ObjectId(hiveId),
  })
    .lean()
    .exec();

  if (!plan) return null;

  // Fetch current workspace state
  const context = await getProjectContext(hiveId);
  const nodes = context.nodes || [];
  const edges = context.edges || [];
  const activeDocs = context.activeDocuments || [];

  // --- Compute current baseline stats ---
  const cycles = detectCycles(nodes, edges);
  const currentCycleCount = cycles.length;

  const docFeatureNodeIds = new Set((activeDocs as WorkspaceDoc[]).map((d) => d.nodeId).filter(Boolean));
  const features = (nodes as WorkspaceNode[]).filter((n) => n.category === "Feature");
  const currentGapCount = features.filter((f) => !docFeatureNodeIds.has(f.id)).length;
  const currentBlockedCount = (nodes as WorkspaceNode[]).filter((n) => n.data?.status === "Blocked").length;

  // --- Simulate step effects ---
  let nodesAdded = 0;
  let nodesModified = 0;
  let nodesDeleted = 0;
  let edgesAdded = 0;
  let edgesDeleted = 0;
  let ownershipChanges = 0;
  let docsAdded = 0;

  for (const step of plan.steps) {
    switch (step.actionType) {
      case "create_node":
        nodesAdded++;
        break;
      case "delete_node":
        nodesDeleted++;
        break;
      case "update_node":
        nodesModified++;
        if (step.params?.updates?.owner !== undefined) ownershipChanges++;
        break;
      case "create_edge":
        edgesAdded++;
        break;
      case "delete_edge":
        edgesDeleted++;
        break;
      case "assign_owner":
        ownershipChanges++;
        nodesModified++;
        break;
      case "create_document":
        docsAdded++;
        nodesAdded++; // Document creates a logical node
        break;
    }
  }

  const dependencyChanges = edgesAdded + edgesDeleted;

  // --- Estimate diagnostic impact ---
  // Risks resolved
  const cycleResolvedByPlan =
    plan.steps.some((s) => s.actionType === "delete_edge") && plan.sourceRiskIds.some((id) => id.startsWith("risk-cycle-"))
      ? Math.min(currentCycleCount, 1)
      : 0;

  const blockedResolvedByPlan =
    plan.steps.some((s) => s.actionType === "assign_owner" || s.actionType === "update_node") &&
    plan.sourceRiskIds.some((id) => id.startsWith("risk-blocked-"))
      ? Math.min(currentBlockedCount, 1)
      : 0;

  const risksResolved = cycleResolvedByPlan + blockedResolvedByPlan;
  const risksCreated = 0; // Plans don't create new risks by design

  // Gaps resolved
  const gapsResolvedByPlan =
    plan.steps.some((s) => s.actionType === "create_document") &&
    plan.sourceGapIds.some((id) => id.startsWith("gap-prd-"))
      ? Math.min(currentGapCount, 1)
      : 0;

  const techGapsResolvedByPlan =
    plan.steps.some((s) => s.actionType === "create_edge") &&
    plan.sourceGapIds.some((id) => id.startsWith("gap-tech-"))
      ? Math.min(currentGapCount - gapsResolvedByPlan, 1)
      : 0;

  const gapsResolved = gapsResolvedByPlan + techGapsResolvedByPlan;
  const gapsCreated = 0;

  // --- Estimate health & momentum delta ---
  // Health formula mirrors hiveMindService.ts scoring
  let estimatedHealthImpact = 0;
  estimatedHealthImpact += risksResolved * 5;          // +5 per risk resolved
  estimatedHealthImpact += gapsResolved * 3;           // +3 per gap closed
  estimatedHealthImpact += ownershipChanges * 2;       // +2 per ownership assigned
  estimatedHealthImpact += cycleResolvedByPlan * 15;   // +15 per cycle fixed (inverse of the -15 penalty)
  estimatedHealthImpact += docsAdded * 3;              // +3 per doc linked (inverse of linked PRDs score)

  // Momentum: ownership changes and activity boosts momentum
  const estimatedMomentumImpact =
    ownershipChanges * 3 + risksResolved * 5 + gapsResolved * 2;

  // --- Affected entity summary ---
  const affectedEntitySet = new Map<string, { entityId: string; entityType: string; title: string; changeType: string }>();
  for (const step of plan.steps) {
    const changeType =
      step.actionType === "delete_node" || step.actionType === "delete_edge"
        ? "removed"
        : step.actionType === "assign_owner" || step.actionType === "update_node"
        ? "modified"
        : "added";

    for (const ent of step.affectedEntities) {
      if (!affectedEntitySet.has(ent.entityId)) {
        affectedEntitySet.set(ent.entityId, { ...ent, changeType });
      }
    }
  }

  const affectedEntities = Array.from(affectedEntitySet.values());

  // --- Reversibility summary ---
  const reversibleSteps = plan.steps.filter((s) => s.reversibility === "reversible").length;
  const irreversibleSteps = plan.steps.length - reversibleSteps;

  const overallRiskLevel: "low" | "medium" | "high" | "critical" = plan.riskLevel;

  return {
    planId,
    nodesAdded,
    nodesModified,
    nodesDeleted,
    risksResolved,
    risksCreated,
    gapsResolved,
    gapsCreated,
    ownershipChanges,
    dependencyChanges,
    estimatedHealthImpact,
    estimatedMomentumImpact,
    affectedEntityCount: affectedEntities.length,
    affectedEntities,
    reversibleSteps,
    irreversibleSteps,
    overallRiskLevel,
  };
}

// ---------------------------------------------------------------------------
// Approve / Reject an Action Plan
// ---------------------------------------------------------------------------

export async function approveActionPlan(
  hiveId: string,
  planId: string,
  actorId: string,
  actorName: string,
  notes?: string
): Promise<IAgentActionPlan | null> {
  const plan = await AgentActionPlan.findOne({
    _id: new mongoose.Types.ObjectId(planId),
    hiveId: new mongoose.Types.ObjectId(hiveId),
    status: "proposed",
  }).exec();

  if (!plan) return null;

  const now = new Date();
  plan.status = "approved";
  plan.approvedBy = new mongoose.Types.ObjectId(actorId);
  plan.approvedAt = now;
  plan.decisionNotes = notes;
  plan.structuredAuditLogs.push({
    actorId: new mongoose.Types.ObjectId(actorId),
    actorName,
    action: "approve",
    notes: notes || "Plan approved.",
    timestamp: now,
  });

  await plan.save();
  return plan;
}

export async function rejectActionPlan(
  hiveId: string,
  planId: string,
  actorId: string,
  actorName: string,
  notes?: string
): Promise<IAgentActionPlan | null> {
  const plan = await AgentActionPlan.findOne({
    _id: new mongoose.Types.ObjectId(planId),
    hiveId: new mongoose.Types.ObjectId(hiveId),
    status: "proposed",
  }).exec();

  if (!plan) return null;

  const now = new Date();
  plan.status = "rejected";
  plan.rejectedBy = new mongoose.Types.ObjectId(actorId);
  plan.rejectedAt = now;
  plan.decisionNotes = notes;
  plan.structuredAuditLogs.push({
    actorId: new mongoose.Types.ObjectId(actorId),
    actorName,
    action: "reject",
    notes: notes || "Plan rejected.",
    timestamp: now,
  });

  await plan.save();
  return plan;
}

// ---------------------------------------------------------------------------
// Expire Stale Plans (for cron / background runs)
// ---------------------------------------------------------------------------

/**
 * Marks all proposed plans that have passed their expiresAt date as expired.
 * Should be called at the start of each generateActionPlans run and/or via a cron job.
 */
export async function expireStalePlans(hiveId?: string): Promise<number> {
  const now = new Date();
  const filter: Record<string, unknown> = {
    status: "proposed",
    expiresAt: { $lt: now },
  };
  if (hiveId) {
    (filter as Record<string, unknown>).hiveId = new mongoose.Types.ObjectId(hiveId);
  }

  const plans = await AgentActionPlan.find(filter).exec();

  let expiredCount = 0;
  for (const plan of plans) {
    plan.status = "expired";
    plan.structuredAuditLogs.push({
      actorId: undefined,
      actorName: "HiveMind Expiry Engine",
      action: "expire",
      notes: `Plan automatically expired at ${now.toISOString()} (was due ${plan.expiresAt.toISOString()}).`,
      timestamp: now,
    });
    await plan.save();
    expiredCount++;
  }

  return expiredCount;
}
