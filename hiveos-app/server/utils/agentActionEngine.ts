function planFromRecommendation(
  rec: {
    _id: mongoose.Types.ObjectId;
    title: string;
    reason: string;
    type: string;
    confidence: number;
    relatedEntities: Array<{ entityId: string; entityType: string; title: string }>;
    suggestedActions: string[];
  },
  expiryHours: number
): Partial<IAgentActionPlan> {
  const entity = rec.relatedEntities[0];
  const steps: IActionStep[] = [];

  // Map suggestion actions to concrete steps
  if (rec.type === "document" && entity) {
    steps.push({
      stepNumber: 1,
      actionType: "create_document",
      params: {
        title: `Document: ${entity.title}`,
        type: "prd",
        linkedNodeId: entity.entityId,
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 3),
      expectedImpact: rec.suggestedActions[0] || "Creates a documentation artifact.",
    });
  } else if (rec.type === "relationship" && entity) {
    steps.push({
      stepNumber: 1,
      actionType: "create_edge",
      params: {
        sourceId: entity.entityId,
        targetId: "__RECOMMENDED_TARGET__",
        relationType: "depends_on",
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 3),
      expectedImpact: rec.suggestedActions[0] || "Creates a dependency relationship.",
    });
  } else if (rec.type === "owner" && entity) {
    steps.push({
      stepNumber: 1,
      actionType: "assign_owner",
      params: {
        nodeId: entity.entityId,
        owner: null, // Never auto-assign
        reason: rec.reason,
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 2),
      expectedImpact: rec.suggestedActions[0] || "Assigns ownership to the node.",
    });
  } else if (rec.type === "architecture" && entity) {
    steps.push({
      stepNumber: 1,
      actionType: "create_node",
      params: {
        category: "Tech Stack",
        title: `Tech Stack for ${entity.title}`,
        owner: null,
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 2),
      expectedImpact: rec.suggestedActions[0] || "Adds tech stack node for the architecture component.",
    });
    steps.push({
      stepNumber: 2,
      actionType: "create_edge",
      params: {
        sourceId: entity.entityId,
        targetId: "__NEW_TECH_STACK_NODE__",
        relationType: "depends_on",
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 2),
      expectedImpact: "Links the architecture node to its implementation stack.",
    });
  } else if (entity) {
    // Generic fallback step
    steps.push({
      stepNumber: 1,
      actionType: "update_node",
      params: {
        nodeId: entity.entityId,
        updates: { status: "In Progress" },
      },
      reversibility: "reversible",
      affectedEntities: rec.relatedEntities.slice(0, 2),
      expectedImpact: rec.suggestedActions[0] || "Updates the node based on recommendation.",
    });
  }

  if (steps.length === 0) return {};

  const affectedEntityCount = steps.reduce((acc, s) => acc + s.affectedEntities.length, 0);
  const riskLevel: "low" | "medium" | "high" | "critical" =
    rec.type === "relationship" ? "medium" : "low";

  return {
    title: `Execute Recommendation: ${rec.title}`,
    description: `Action plan derived from HiveMind recommendation. Reason: ${rec.reason}`,
    reason: rec.reason,
    confidence: rec.confidence,
    riskLevel,
    riskScore: computeRiskScore(riskLevel),
    actionQualityScore: computeActionQualityScore(rec.confidence, steps, affectedEntityCount),
    steps,
    sourceRiskIds: [],
    sourceGapIds: [],
    sourceRecommendationIds: [rec._id],
    sourceMissionIds: [],
    expiresAt: makeExpiresAt(expiryHours),
    structuredAuditLogs: [buildAuditLog("HiveMind Action Engine")],
  };
}

