function planFromGapFeatureNoPrd(
  gap: KnowledgeGap,
  expiryHours: number
): Partial<IAgentActionPlan> {
  const entity = gap.relatedEntities[0];
  if (!entity) return {};

  const steps: IActionStep[] = [
    {
      stepNumber: 1,
      actionType: "create_document",
      params: {
        title: `PRD: ${entity.title}`,
        type: "prd",
        linkedNodeId: entity.entityId,
        templateHint: "prd_template",
      },
      reversibility: "reversible",
      affectedEntities: [entity],
      expectedImpact: "Creates a structured PRD document and links it to the feature node.",
    },
    {
      stepNumber: 2,
      actionType: "create_node",
      params: {
        category: "Task",
        title: `Implementation Tasks: ${entity.title}`,
        status: "Todo",
        linkedFeatureId: entity.entityId,
        owner: null, // No auto-assignment — ownership must be proposed separately
      },
      reversibility: "reversible",
      affectedEntities: [entity],
      expectedImpact: "Creates a task node scaffold for implementation tracking.",
    },
  ];

  const affectedEntityCount = steps.reduce((acc, s) => acc + s.affectedEntities.length, 0);
  const riskLevel = "low";
  const confidence = 0.85;

  return {
    title: `Resolve Documentation Gap: ${entity.title}`,
    description: `Feature node "${entity.title}" exists without a PRD. This plan creates a document stub and a task node to resolve the gap.`,
    reason: `Gap detected: feature_no_prd. A feature without a spec creates unclear implementation expectations and reduces health score.`,
    confidence,
    riskLevel,
    riskScore: computeRiskScore(riskLevel),
    actionQualityScore: computeActionQualityScore(confidence, steps, affectedEntityCount),
    steps,
    sourceGapIds: [gap.id],
    sourceRiskIds: [],
    sourceRecommendationIds: [],
    sourceMissionIds: [],
    expiresAt: makeExpiresAt(expiryHours),
    structuredAuditLogs: [buildAuditLog("HiveMind Action Engine")],
  };
}

